/**
 * Regenerates the bundled demo cascade under public/examples/.
 *
 * The cascade is wired by real SHA-1 checksums, so the files must be written
 * bottom-up (leaf documents first, then the documents that reference them).
 * acme-identity ships under extra/ but is intentionally NOT auto-loaded by
 * the "Load example" button — it demos the unresolved-placeholder flow, and
 * its relative URL lets the in-app "Fetch from URL" resolve it same-origin.
 *
 *   node scripts/generate-examples.mjs
 */
import { createHash } from 'node:crypto';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';

const outDir = new URL('../public/examples/', import.meta.url);
mkdirSync(new URL('extra/', outDir), { recursive: true });

const sha1 = (text) => createHash('sha1').update(text, 'utf8').digest('hex');
const write = (name, text) => {
  writeFileSync(new URL(name, outDir), text);
  console.log(`${name}  (${text.length} bytes, sha1 ${sha1(text)})`);
  return sha1(text);
};

