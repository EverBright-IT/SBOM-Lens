function runtimeImage() {
  const version = (i) => `${1 + (i % 3)}.${(i * 7) % 30}.${(i * 13) % 10}`;
  const lines = [
    'SPDXVersion: SPDX-2.3',
    'DataLicense: CC0-1.0',
    'SPDXID: SPDXRef-DOCUMENT',
    'DocumentName: acme-runtime-image',
    'DocumentNamespace: https://example.org/spdxdocs/acme-runtime-image-3.0',
    'Creator: Organization: ACME Corp',
    'Creator: Tool: acme-image-scanner-2.4',
    'Created: 2026-05-10T08:30:00Z',
    'DocumentComment: <text>Per-image SBOM of the acme runtime container. Generated by the demo fixture generator; all data is synthetic.</text>',
    '',
    'PackageName: acme-runtime-image',
    'SPDXID: SPDXRef-Package-runtime-image',
    'PackageVersion: 3.0.0',
    'PackageDownloadLocation: NOASSERTION',
    'PrimaryPackagePurpose: CONTAINER',
    'PackageSupplier: Organization: ACME Corp',
    '',
  ];
  const ids = [];
  BASE_LIBS.forEach((name, i) => {
    // Suffix variants pad the list to ~120 packages, like real image scans.
    for (const suffix of ['', '-dev'].slice(0, i % 3 === 0 ? 2 : 1)) {
      const id = `SPDXRef-Package-${name}${suffix}`.replaceAll('+', 'x');
      const v = version(i);
      ids.push({ id, name: `${name}${suffix}` });
      lines.push(
        `PackageName: ${name}${suffix}`,
        `SPDXID: ${id}`,
        `PackageVersion: ${v}`,
        'PackageDownloadLocation: NOASSERTION',
        'PrimaryPackagePurpose: LIBRARY',
        `PackageLicenseConcluded: ${LICENSES[i % LICENSES.length]}`,
        `PackageSupplier: Organization: Alpine Linux`,
        `ExternalRef: PACKAGE-MANAGER purl pkg:apk/alpine/${encodeURIComponent(name + suffix)}@${v}?distro=3.20`,
        '',
      );
    }
  });
  // A deliberate duplicate block — dedupe with a diagnostic, like trivy output.
  lines.push(
    'PackageName: busybox',
    'SPDXID: SPDXRef-Package-busybox',
    'PackageVersion: 1.4.7',
    'PackageDownloadLocation: NOASSERTION',
    '',
  );

  lines.push('Relationship: SPDXRef-DOCUMENT DESCRIBES SPDXRef-Package-runtime-image');
  for (const { id } of ids) {
    lines.push(`Relationship: SPDXRef-Package-runtime-image CONTAINS ${id}`);
  }
  for (const [from, to] of DEPENDS) {
    lines.push(`Relationship: SPDXRef-Package-${to.replaceAll('+', 'x')} DEPENDENCY_OF SPDXRef-Package-${from.replaceAll('+', 'x')}`);
  }
  return lines.join('\n') + '\n';
}

